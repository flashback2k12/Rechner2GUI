package Main;

public class CalcOperator {

	public static double addiere2 (double zahl1, double zahl2) {
		
		double ergebnis = zahl1 + zahl2;
	
		return ergebnis; 
	}
	
         /*
         * new Operations
         */

         public static double sub2 (double zahl1, double zahl2) {
		
		double ergebnis = zahl1 - zahl2;
	
		return ergebnis; 
	}

         public static double mul2 (double zahl1, double zahl2) {
		
		double ergebnis = zahl1 * zahl2;
	
		return ergebnis; 
	}

         public static double div2 (double zahl1, double zahl2) {
		
		double ergebnis = zahl1 / zahl2;
	
		return ergebnis; 
	}


}
